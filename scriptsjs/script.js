        const setLanguage = (lang) => {
            document.querySelectorAll('[data-key]').forEach(element => {
                const key = element.getAttribute('data-key');
                if (translations[lang][key]) {
                    // Handle innerHTML for elements with HTML content (like hero-title)
                    if (key === 'hero-title' || key === 'about-us-trust') {
                        element.innerHTML = translations[lang][key];
                    } else {
                        element.textContent = translations[lang][key];
                    }
                }
            });
            document.getElementById('currentLanguage').textContent = lang.toUpperCase();
            // Store selected language in localStorage
            localStorage.setItem('selectedLanguage', lang);
        };

        // Event listeners for language dropdown
        document.querySelectorAll('.dropdown-item').forEach(item => {
            item.addEventListener('click', (e) => {
                e.preventDefault();
                const lang = e.target.getAttribute('data-lang');
                setLanguage(lang);
            });
        });

        // Set initial language based on localStorage or default to ES
        document.addEventListener('DOMContentLoaded', () => {
            const storedLang = localStorage.getItem('selectedLanguage');
            if (storedLang) {
                setLanguage(storedLang);
            } else {
                setLanguage('es'); // Default language
            }
        });
